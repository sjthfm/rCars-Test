
public class VehicleStat {
	
	private String SIPP, Price, Supplier, Name, Rating;
	
	public VehicleStat(String SIPP,String Price,String Supplier,String Name,String Rating) {
		this.SIPP 		= SIPP;
		this.Price 		= Price;
		this.Supplier 	= Supplier;
		this.Name 		= Name;
		this.Rating 	= Rating;
	}
	
}
